function multiplica(x, y) {
  // La función recibe dos argumentos llamados "x" e "y" que son números.
  // Retorna el resultado de la multiplicacion de estos argumentos.
  // Por ejemplo: 
  // 10, 5 ---> 50
  // 5, 5 ---> 25
  // Tu código:
  return x * y;
}

multiplica(1, 3); //3

multiplica(2, 6); //12

multiplica(-3, 4); //-12

module.exports = multiplica;